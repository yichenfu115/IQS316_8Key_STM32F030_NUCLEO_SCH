/** Configure pins as
		* Analog
		* Input
		* Output
		* EVENT_OUT
		* EXTI
		*/
void MX_GPIO_Init(void)
{

	GPIO_InitTypeDef GPIO_InitStruct;

	/* GPIO Ports Clock Enable */
	__HAL_RCC_GPIOA_CLK_ENABLE();
	__HAL_RCC_GPIOB_CLK_ENABLE();

	/*Configure GPIO pin Output Level */
	HAL_GPIO_WritePin(GPIOA, LED3_Pin | SPI1_SS_Pin | MCLR_Pin, GPIO_PIN_RESET);

	/*Configure GPIO pins : LED3_Pin SPI1_SS_Pin MCLR_Pin */
	GPIO_InitStruct.Pin = LED3_Pin | SPI1_SS_Pin | MCLR_Pin;
	GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
	GPIO_InitStruct.Pull = GPIO_NOPULL;
	GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
	HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

	/*Configure GPIO pin : POUT_Pin  TOUT_Pin */
	GPIO_InitStruct.Pin = POUT_Pin | TOUT_Pin;
	GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
	GPIO_InitStruct.Pull = GPIO_NOPULL;
	HAL_GPIO_Init(POUT_GPIO_Port, &GPIO_InitStruct);

	/*Configure GPIO pin : RDY_Pin */
	GPIO_InitStruct.Pin = RDY_Pin;
	GPIO_InitStruct.Mode = GPIO_MODE_INPUT;
	GPIO_InitStruct.Pull = GPIO_NOPULL;
	HAL_GPIO_Init(RDY_GPIO_Port, &GPIO_InitStruct);



}

